      <p>Sistem pendukung keputusan (SPK) merupakan sistem informasi interaktif yang menyediakan informasi, permodelan dan pemanipulasian data. Sistem itu digunakan untuk membantu pengambilan keputusan dalam situasi yang tidak terstruktur, dimana tidak seorangpun tau secara pasti bagaimana keputusan dibuat.</p>
      <p>Metode VIKOR (ViseKriterijumska Optimizacija I Kompromisno Resenje) adalah salah satu metode pengambilan keputusan multi kriteria atau yang lebih dikenal dengan istilah Multi Criteria Decision Making (MCDM). MCDM digunakan untuk menyelesaikan permasalahan dengan kriteria yang  bertentangan dan tidak sepadan. Metode ini berfokus pada peringkat dan pemilihan dari sekumpulan alternatif kriteria yang saling bertentangan untuk dapat mengambil keputusan untuk mencapai keputusan akhir </p>
